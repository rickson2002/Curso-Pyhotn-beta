# Operadores Aritméticos (Matemática)

adicao = 10 + 10
print("Adição", adicao)

subtracao = 10 - 5
print("Subtração", subtracao)

multiplicacao = 10 * 10
print("Multiplicação", multiplicacao)

divisao = 10 / 2.2
print("Divisao", divisao)

divisao_inteira = 20 // 2.2
print("Divisao Intteira", divisao_inteira)

expotenciacao = 2 ** 10
print("Exponenciaçao", expotenciacao)

modulo = 55 % 2 # resto da divisao
print("Modulo", modulo)
