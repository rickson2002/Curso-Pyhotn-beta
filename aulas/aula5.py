# Tipo de dado boll ( boolean)
# Ao questionar algo em um programa
# Só existe duas respostas possiveis:
# Sim ( true ) ou não ( False ).
# Existe vários operadores para "questionar".
# Dentro eles o ==, que é um operador lógico que
# Questiona se um valor é igual a outro
print(10 == 10) # Sim => True (Verdadeiro)
print(10 == 11) # Não => False (falso)
print(type(True))
print(type(False))
print(type(10 == 10))
print(type(10 == 11)) 